package Assighment1;

public class SeriesPrinter {
	 void printSeries() {
	        for (int i = 1; i <= 10; i++)
	            System.out.print(i + " ");
	    }

	    public static void main(String[] args) {
	        SeriesPrinter sp = new SeriesPrinter();
	        sp.printSeries();
	    }

}
